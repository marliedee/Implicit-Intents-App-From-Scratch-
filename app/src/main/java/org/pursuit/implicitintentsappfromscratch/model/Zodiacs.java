package org.pursuit.implicitintentsappfromscratch.model;

public class Zodiacs {

    private String name;
    private String number;
    private String image;

    public Zodiacs(String name, String number, String image) {
        this.name = name;
        this.number = number;
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public String getNumber() {
        return number;
    }

    public String getImage() {
        return image;
    }


}
